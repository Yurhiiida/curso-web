//Crie uma função que dado dois valores(passados como parâmetro) mostre no console a soma, subtração, multiplicação e divisão desses valor.

function resultado(a, b) {
    console.log(`A soma de ${a} + ${b} é ${a + b}`)
    console.log(`A subtração de ${a} - ${b} é ${a - b}`)
    console.log(`A multiplicação de ${a} * ${b} é ${a * b}`)
    console.log(`A divisão de ${a} / ${b} é ${a / b}`)
}

resultado(10, 25)