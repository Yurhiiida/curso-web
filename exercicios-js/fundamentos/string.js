console.log(escola.charAt(4))//retorna o caractere do index especificado

console.log(escola.charCodeAt(3))//retorna o unicode do caractere do index especificado

console.log(escola.indexOf('d'))// retorna a posição do numero ou letra do especificado.

console.log(escola.substring(2))// retorna a substring apartir do index especificado

console.log(escola.substring(0, 3))// retorna a substring da posição especificada até a posição especificada

console.log(escola.replace(3, 'e')) //faz troca de uma letra na string por outra
